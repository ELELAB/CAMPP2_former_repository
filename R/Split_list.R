#' @title Split list
#' @description Spliting list of files using ","
#' @param my.list list
#' @export
#' @return split list
#' @examples \dontrun{
#' SplitList (my.list)
#' }



# ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
# Spliting list
# ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

SplitList <- function(my.list) {
    my.list <- as.character(unlist(strsplit(my.list, split=",")))
    return(my.list)
}
